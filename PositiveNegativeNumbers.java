package week1.day2;

public class PositiveNegativeNumbers {

	public static void main(String[] args) {
    int num = -45;
    if (num>=0)
    {
    	System.out.println("The Given Number "+num+ " is Positive");
    }
    else
    {
    	System.out.println("The Given Number "+num+ " is Negative");
    }
	}

}
